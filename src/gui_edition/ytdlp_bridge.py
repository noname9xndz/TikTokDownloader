"""yt-dlp powered TikTok download backend.

Provides async wrappers around yt-dlp for downloading TikTok videos.
Used as a fallback when the native API is blocked by TikTok's WAF.

The cookie is read from ``parameter.cookie_dict_tiktok`` and converted
to a Netscape cookie file for yt-dlp consumption.
"""
from __future__ import annotations

import asyncio
import json
import os
import subprocess
import sys
import tempfile
from pathlib import Path
from typing import TYPE_CHECKING, List, Optional

if TYPE_CHECKING:
    from src.config import Parameter
    from src.record import BaseLogger, LoggerManager

__all__ = ["YtDlpTikTok"]

